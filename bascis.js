// Stack creation using an array
let stack = [];

// Pushing elements into the stack
stack.push(2);
stack.push(3);
stack.push(5);

// Popping the top element from the stack
stack.pop(); // removes 5

// Peek value of the stack (top element)
console.log(stack[stack.length - 1]); // 3

// Print the entire stack
console.log(stack); // [2, 3]
 